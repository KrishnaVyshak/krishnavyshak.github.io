/**
 * gen-logo-map.mjs
 * Reads the logo PNG, scales to COLS×ROWS grid, applies threshold,
 * and outputs a TypeScript const declaration of the boolean map.
 *
 * Usage: node scripts/gen-logo-map.mjs
 */

import Jimp from "jimp";
import { writeFileSync } from "fs";
import { fileURLToPath } from "url";
import { dirname, join } from "path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const IMAGE_PATH = join(__dirname, "..", "public", "logo.png");
const OUT_PATH   = join(__dirname, "..", "components", "logoMap.ts");

// Grid dimensions — cols × rows
// Monospace chars are ~0.5 wide : 1 tall, so COLS = ROWS / 2 for square logo
const ROWS = 60;
const COLS = 30;

// Brightness threshold: pixels BELOW this (darker) are "filled"
// Logo is black on white → dark = logo shape
const THRESHOLD = 128;

async function main() {
  const img = await Jimp.read(IMAGE_PATH);

  // Crop whitespace bounding the black logo (auto-crop)
  img.autocrop({ tolerance: 0.02 });

  // Resize to grid dimensions
  img.resize(COLS, ROWS, Jimp.RESIZE_BILINEAR);
  img.greyscale();

  const map = [];
  for (let r = 0; r < ROWS; r++) {
    const row = [];
    for (let c = 0; c < COLS; c++) {
      const pixel = Jimp.intToRGBA(img.getPixelColor(c, r));
      // pixel.r == grey value (greyscale applied)
      row.push(pixel.r < THRESHOLD ? 1 : 0);
    }
    map.push(row);
  }

  // Print ASCII preview to console
  console.log("\n=== Logo Preview ===");
  for (const row of map) {
    console.log(row.map(v => v ? "█" : " ").join(""));
  }
  console.log("===================\n");

  // Output TypeScript file
  const ts = `// AUTO-GENERATED by gen-logo-map.mjs — do not edit manually
export const LOGO_ROWS = ${ROWS};
export const LOGO_COLS = ${COLS};
export const LOGO_MAP: number[][] = ${JSON.stringify(map)};
`;
  writeFileSync(OUT_PATH, ts, "utf8");
  console.log(`Written to ${OUT_PATH}`);
}

main().catch(e => { console.error(e); process.exit(1); });
